#include "push_swap.h"

int	count_char(char *str, char c)
{
	int	i;
	int	j;

	if (!str)
		return (0);
	i = 0;
	j = 0;
	while (str[j])
	{
		if (str[j] == c)
			i++;
		j++;
	}
	return (i);
}
